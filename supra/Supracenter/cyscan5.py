def angleErr(x, *cyscan_inputs):
    S, z_profile, D, wind, debug, h_tol, v_tol = cyscan_inputs

    r = anglescan(S, x[0], x[1], z_profile, trace=False, debug=debug, wind=wind)

    err = np.sqrt((D[0] - r[0])**2 + (D[1] - r[1])**2 + (D[2] - r[2])**2)

    return err

